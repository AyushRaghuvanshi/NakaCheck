class Vehicle {
  String? company;
  String? number;
  String? color;
  String? regDate;
  String? picture;
  String? regUnder;

  Vehicle(String company, String number, String color, String regDate,
      String regUnder, String picture) {
    this.company = company;
    this.number = number;
    this.color = color;
    this.regDate = regDate;
    this.regUnder = regUnder;
    this.picture = picture;
  }
}
